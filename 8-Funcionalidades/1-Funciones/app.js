//Funciones son bloques de código reutilizables que realizan una tarea específica.
// Estructura básica de una función
function saludar(nombre) {
    console.log(`Hola, ${nombre}`);
}

// Llamada a la función
saludar("Soriyama"); // Hola, Soriyama

// Estructura flecha de una función
const saludarFlecha = (nombre) => {
    console.log(`Hola, ${nombre}`);
}
// Llamada a la función flecha
saludarFlecha("Soriyama"); // Hola, Soriyama

/*Ventajas de la función flecha:
1. Sintaxis más concisa.
2. Ideal para funciones de una sola línea.
*/